def solid_fill_pattern_id(new_doc):
    solid_fill_id = None
    fillpatterns = DB.FilteredElementCollector(new_doc).OfClass(
        DB.FillPatternElement
    )
    for pat in fillpatterns:
        if pat.GetFillPattern().IsSolidFill:
            solid_fill_id = pat.Id
            break
    return solid_fill_id


class SubscribeView(UI.IExternalEventHandler):
    def __init__(self):
        self.registered = 1

    def Execute(self, uiapp):
        try:
            if self.registered == 1:
                self.registered = 0
                uiapp.ViewActivated += self.view_changed
            else:
                self.registered = 1
                uiapp.ViewActivated -= self.view_changed
        except Exception:
            pass

    def view_changed(self, sender, e):
        if wndw.IsOpen == 1:
            if self.registered == 0:
                new_doc = e.Document
                if new_doc:
                    new_uiapp = new_doc.Application
                    if wndw:
                        if not new_doc.Equals(doc):
                            wndw.Close()
                # Update categories in dropdown
                new_view = get_active_view(e.Document)
                if new_view != 0:
                    # Unsubcribe
                    wndw._list_box2.SelectedIndexChanged -= (
                        wndw.list_selected_index_changed
                    )
                    # Update categories for new view
                    wndw.crt_view = new_view
                    categ_inf_used_up = get_used_categories_and_parameters(
                        cat_excluded, wndw.crt_view
                    )
                    wndw._categories_table_data = DataTable("Data")
                    wndw._categories_table_data.Columns.Add("Key", String)
                    wndw._categories_table_data.Columns.Add("Value", Object)
                    names = [x._name for x in categ_inf_used_up]
                    wndw._categories_table_data.Rows.Add("Select a Category Here!", 0)
                    [
                        wndw._categories_table_data.Rows.Add(key_, value_)
                        for key_, value_ in zip(names, categ_inf_used_up)
                    ]
                    wndw._categories.DataSource = wndw._categories_table_data
                    wndw._categories.DisplayMember = "Key"
                    # Vaciar range of values
                    wndw._table_data3 = DataTable("Data")
                    wndw._table_data3.Columns.Add("Key", String)
                    wndw._table_data3.Columns.Add("Value", Object)
                    wndw._list_box2.DataSource = wndw._table_data3
                    wndw._list_box2.DisplayMember = "Key"

    def GetName(self):
        return "Subscribe View Changed Event"


class ApplyColors(UI.IExternalEventHandler):
    def __init__(self):
        pass

    def Execute(self, uiapp):
        try:
            new_doc = revit.DOCS.doc
            view = get_active_view(new_doc)
            if view != 0:
                sf = solid_fill_pattern_id(new_doc)
                t = DB.Transaction(new_doc, "Apply colors to elements")
                t.Start()
                sel_cat = wndw._categories.SelectedItem["Value"]
                if (
                    sel_cat._cat.Id.IntegerValue == int(DB.BuiltInCategory.OST_Rooms)
                    or sel_cat._cat.Id.IntegerValue
                    == int(DB.BuiltInCategory.OST_MEPSpaces)
                    or sel_cat._cat.Id.IntegerValue == int(DB.BuiltInCategory.OST_Areas)
                ):
                    # In case of rooms, spaces and areas. Check Color scheme is applied and if not
                    if version > 2021:
                        if (
                            str(wndw.crt_view.GetColorFillSchemeId(sel_cat._cat.Id))
                            == "-1"
                        ):
                            fColorScheme = (
                                DB.FilteredElementCollector(new_doc)
                                .OfClass(DB.ColorFillScheme)
                                .ToElements()
                            )
                            if len(fColorScheme) > 0:
                                for sch in fColorScheme:
                                    if (
                                        sch.CategoryId.IntegerValue
                                        == sel_cat._cat.Id.IntegerValue
                                    ):
                                        if len(sch.GetEntries()) > 0:
                                            wndw.crt_view.SetColorFillSchemeId(
                                                sel_cat._cat.Id, sch.Id
                                            )
                                            break
                    else:
                        wndw._txt_block5.Visible = True
                else:
                    wndw._txt_block5.Visible = False

                for indx in range(wndw._list_box2.Items.Count):
                    ogs = DB.OverrideGraphicSettings().Dispose()
                    ogs = DB.OverrideGraphicSettings()
                    color = DB.Color(
                        wndw._list_box2.Items[indx]["Value"]._n1,
                        wndw._list_box2.Items[indx]["Value"]._n2,
                        wndw._list_box2.Items[indx]["Value"]._n3,
                    )
                    # ogs.SetProjectionLineColor(color)
                    ogs.SetSurfaceForegroundPatternColor(color)
                    ogs.SetCutForegroundPatternColor(color)
                    if sf is not None:
                        ogs.SetSurfaceForegroundPatternId(sf)
                        ogs.SetCutForegroundPatternId(sf)
                    ogs.SetProjectionLinePatternId(DB.ElementId(-1))
                    for id in wndw._list_box2.Items[indx]["Value"]._eleId:
                        view.SetElementOverrides(id, ogs)
                t.Commit()
        except Exception:
            pass

    def GetName(self):
        return "Apply colors to elements"


