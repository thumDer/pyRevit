class CreateFilters(UI.IExternalEventHandler):
    def __init__(self):
        pass

    def Execute(self, uiapp):
        try:
            new_doc = revit.DOCS.doc
            view = get_active_view(new_doc)
            with revit.Transaction(doc=new_doc, name="Create View Filters"):
                sel_cat = wndw._categories.SelectedItem["Value"]
                sel_par = wndw._list_box1.SelectedItem["Value"]
                categories = List[DB.ElementId]()
                categories.Add(sel_cat._cat.Id)
                sf = solid_fill_pattern_id(new_doc)
                for i, element in enumerate(wndw._list_box2.Items):
                    item = wndw._list_box2.Items[i]["Value"]

                    # Assign color filled region
                    ogs = DB.OverrideGraphicSettings().Dispose()
                    ogs = DB.OverrideGraphicSettings()
                    color = DB.Color(item._n1, item._n2, item._n3)
                    ogs.SetSurfaceForegroundPatternColor(color)
                    ogs.SetCutForegroundPatternColor(color)
                    ogs.SetSurfaceForegroundPatternId(sf)
                    ogs.SetCutForegroundPatternId(sf)

                    # filter naming
                    filter_name = (sel_cat._name + " / " + sel_par._name + " - " + item._value)
                    filter_name = filter_name.strip("{}[]:\|?/<>*")

                    # Create filter
                    parameter_id = DB.ElementId(sel_par._rl_par.Id.IntegerValue)
                    param_storage_type = sel_par._rl_par.StorageType
                    if param_storage_type == DB.StorageType.Double:
                        value = float(sub("[^-\d|.]", "", item._value))
                        if version > 2021:
                            value_to_internal = DB.UnitUtils.ConvertToInternalUnits(
                                value, sel_par._rl_par.GetUnitTypeId()
                            )
                        else:
                            value_to_internal = DB.UnitUtils.ConvertToInternalUnits(
                                value, sel_par._rl_par.DisplayUnitType
                            )
                        equals_rule = DB.ParameterFilterRuleFactory.CreateEqualsRule(
                            parameter_id, value_to_internal, 0.5
                        )
                    elif param_storage_type == DB.StorageType.ElementId:
                        equals_rule = DB.ParameterFilterRuleFactory.CreateEqualsRule(
                            parameter_id, item._value
                        )
                    elif param_storage_type == DB.StorageType.Integer:
                        value = int(sub("[^0-9]", "", item._value))
                        equals_rule = DB.ParameterFilterRuleFactory.CreateEqualsRule(
                            parameter_id, value
                        )
                    elif param_storage_type == DB.StorageType.String:
                        try:
                            equals_rule = (
                                DB.ParameterFilterRuleFactory.CreateEqualsRule(
                                    parameter_id, item._value
                                )
                            )
                        except TypeError:  # different method in versions earlier than R2023
                            equals_rule = (
                                DB.ParameterFilterRuleFactory.CreateEqualsRule(
                                    parameter_id, item._value, True
                                )
                            )
                    else:
                        alert(msg="Filter Creation for this Parameter type is not supported: {}".format(sel_par._rl_par.StorageType), title="Error")
                        break
                    elem_filter = DB.ElementParameterFilter(equals_rule)
                    fltr = DB.ParameterFilterElement.Create(
                        new_doc, filter_name, categories, elem_filter
                    )
                    view.AddFilter(fltr.Id)
                    view.SetFilterOverrides(fltr.Id, ogs)
        except Exception as e:
            alert(msg="{}\nFilter Creation for this Parameter type is not supported: {}".format(e, sel_par._rl_par.StorageType), title="Error")
            pass

    def GetName(self):
        return "Create Filters"


class CreateLegend(UI.IExternalEventHandler):
    def __init__(self):
        pass

    def Execute(self, uiapp):
        try:
            new_doc = revit.DOCS.doc
            # Get legend view
            collector = (
                DB.FilteredElementCollector(new_doc).OfClass(DB.View).ToElements()
            )

            legends = []
            for vw in collector:
                if vw.ViewType == DB.ViewType.Legend:
                    legends.append(vw)
                    break
            if len(legends) > 0:
                # Duplicate existing legend
                with revit.TransactionGroup(doc=new_doc, name="Create Legend"):
                    with revit.Transaction(doc=new_doc, name="Duplicate Legend"):
                        sel_cat = wndw._categories.SelectedItem["Value"]
                        sel_par = wndw._list_box1.SelectedItem["Value"]
                        new_id_legend = legends[0].Duplicate(
                            DB.ViewDuplicateOption.Duplicate
                        )
                        new_legend = new_doc.GetElement(new_id_legend)
                        i = 1
                        new_legend_name = (
                            str(
                                "Color Legend {} for View {}".format(
                                    (sel_cat._name + " / " + sel_par._name),
                                    revit.DOCS.doc.ActiveView.Name,
                                )
                            )
                            .replace("{", "")
                            .replace("}", "")
                        )
                        while True:
                            try:
                                new_legend.Name = str(new_legend_name + " - " + str(i))
                                break
                            except Exception:
                                i += 1
                                if i == 100:
                                    break
                    with revit.Transaction(doc=new_doc, name="Create Legend Graphic"):
                        ele_id_type = (
                            DB.FilteredElementCollector(new_doc)
                            .OfClass(DB.TextNoteType)
                            .FirstElementId()
                        )
                        list_max_X = []
                        list_y = []
                        # FilledRegionType
                        filled_type = []
                        all_types = (
                            DB.FilteredElementCollector(new_doc)
                            .OfClass(DB.FilledRegionType)
                            .ToElements()
                        )
                        for type in all_types:
                            pattern = new_doc.GetElement(type.ForegroundPatternId)
                            if pattern is not None:
                                if pattern.GetFillPattern().IsSolidFill:
                                    if type.ForegroundPatternColor.IsValid:
                                        filled_type.append(type)
                                        break
                        # Create Type if none is fill
                        if len(filled_type) == 0 and len(all_types) > 0:
                            it = 1
                            # Duplicate existing fillregiontype
                            while True:
                                try:
                                    new_type = all_types[0].Duplicate(
                                        "Fill Region " + str(it)
                                    )
                                    break
                                except:
                                    it += 1
                                    if it == 100:
                                        break
                            # Create pattern
                            it = 1
                            while True:
                                try:
                                    new_pattern = DB.FillPattern(
                                        "Fill Pattern " + str(it),
                                        DB.FillPatternTarget.Drafting,
                                        DB.FillPatternHostOrientation.ToView,
                                        float(0),
                                        float(0.00001),
                                    )
                                    new_elePat = DB.FillPatternElement.Create(
                                        new_doc, new_pattern
                                    )
                                    break
                                except:
                                    it += 1
                                    if it == 100:
                                        break
                            # Assign to type
                            new_type.ForegroundPatternId = new_elePat.Id
                            filled_type.append(new_type)
                        # Create Title
                        title_loc = DB.XYZ(0, 0, 0)
                        title_text = sel_cat._name + " / " + sel_par._name
                        legend_title = DB.TextNote.Create(
                            new_doc, new_legend.Id, title_loc, title_text, ele_id_type
                        )
                        new_doc.Regenerate()
                        title_bbox = legend_title.get_BoundingBox(new_legend)
                        offset = (title_bbox.Max.Y + title_bbox.Min.Y) * 2
                        # Create Text
                        for vw_item in wndw._list_box2.Items:
                            point = DB.XYZ(0, offset, 0)
                            index = wndw._list_box2.Items.IndexOf(vw_item)
                            if index != 0:
                                point = DB.XYZ(0, fin_coord_y, 0)
                            item = vw_item["Value"]
                            text_line = item._value
                            new_text = DB.TextNote.Create(
                                new_doc, new_legend.Id, point, text_line, ele_id_type
                            )
                            new_doc.Regenerate()
                            prev_bbox = new_text.get_BoundingBox(new_legend)
                            offset = (prev_bbox.Max.Y - prev_bbox.Min.Y) * 0.25
                            fin_coord_y = prev_bbox.Min.Y - offset
                            list_max_X.append(prev_bbox.Max.X)
                            list_y.append(fin_coord_y + offset)
                            height = (prev_bbox.Max.Y - prev_bbox.Min.Y) * 0.8
                        ini_x = max(list_max_X)
                        ogs = DB.OverrideGraphicSettings()
                        # Create filled color region
                        for indx, elem in enumerate(list_y):
                            coord_y = elem
                            item = wndw._list_box2.Items[indx]["Value"]
                            point0 = DB.XYZ(ini_x, coord_y, 0)
                            point1 = DB.XYZ(ini_x, coord_y + height, 0)
                            point2 = DB.XYZ(ini_x * 2, coord_y + height, 0)
                            point3 = DB.XYZ(ini_x * 2, coord_y, 0)
                            line01 = DB.Line.CreateBound(point0, point1)
                            line12 = DB.Line.CreateBound(point1, point2)
                            line23 = DB.Line.CreateBound(point2, point3)
                            line30 = DB.Line.CreateBound(point3, point0)
                            list_curveLoops = List[DB.CurveLoop]()
                            curveLoops = DB.CurveLoop()
                            curveLoops.Append(line01)
                            curveLoops.Append(line12)
                            curveLoops.Append(line23)
                            curveLoops.Append(line30)
                            list_curveLoops.Add(curveLoops)
                            reg = DB.FilledRegion.Create(
                                new_doc,
                                filled_type[0].Id,
                                new_legend.Id,
                                list_curveLoops,
                            )
                            # Assign color filled region
                            color = DB.Color(item._n1, item._n2, item._n3)
                            # ogs.SetProjectionLineColor(color)
                            ogs.SetSurfaceForegroundPatternColor(color)
                            ogs.SetCutForegroundPatternColor(color)
                            # ogs.SetProjectionLinePatternId(DB.ElementId(-1))
                            new_legend.SetElementOverrides(reg.Id, ogs)
            else:
                task2 = UI.TaskDialog("Color Elements by Parameter Value")
                task2.MainInstruction = "In order to create a new legend, you need to have at least one. Please, create a legend view."
                wndw.TopMost = False
                task2.Show()
                wndw.TopMost = True
        except Exception:
            pass

    def GetName(self):
        return "Create Legend"


