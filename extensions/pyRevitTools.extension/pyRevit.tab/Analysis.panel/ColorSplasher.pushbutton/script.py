def get_range_of_values(category, param, new_view):
    for sample_bic in Enum.GetValues(DB.BuiltInCategory):
        if category._intId == int(sample_bic):
            bic = sample_bic
            break
    collector = (
        DB.FilteredElementCollector(doc, new_view.Id)
        .OfCategory(bic)
        .WhereElementIsNotElementType()
        .WhereElementIsViewIndependent()
        .ToElements()
    )
    list_values = []
    # Iterar todos los elementos y conseguir valores unicos
    for ele in collector:
        ele_par = ele
        if param._type == 1:
            ele_par = doc.GetElement(ele.GetTypeId())
        for pr in ele_par.Parameters:
            if pr.Definition.Name == param._par.Name:
                value = get_value_par(pr)
                if value == "" or value is None:
                    value = "N/A"
                match = [x for x in list_values if x._value == value]
                if len(match) > 0:
                    match[0]._eleId.Add(ele.Id)
                else:
                    while True:
                        r, g, b = random_color()
                        match = [
                            x
                            for x in list_values
                            if x._n1 == r and x._n2 == g and x._n3 == b
                        ]
                        if len(match) == 0:
                            val = ValuesInfo(pr, value, ele.Id, r, g, b)
                            list_values.append(val)
                            break
                break
    copy = [x for x in list_values if x._value == "N/A"]
    if len(copy) > 0:
        list_values.remove(copy[0])
    # Ordenar
    list_values.sort(key=lambda x: x._value, reverse=False)
    if len(list_values) > 1:
        try:
            first_value = list_values[0]._value
            indx_del = get_index_units(first_value)
            if indx_del == 0:
                list_values = sorted(list_values, key=lambda x: float(x._value))
            elif indx_del < len(first_value) and indx_del != -1:
                list_values = sorted(
                    list_values, key=lambda x: float(x._value[:-indx_del])
                )
        except Exception:
            pass
    if len(copy) > 0 and len(copy[0]._eleId) > 0:
        list_values.append(copy[0])
    return list_values


def get_used_categories_and_parameters(cat_exc, acti_view):
    # Get All elements and filter unneeded
    collector = (
        DB.FilteredElementCollector(doc, acti_view.Id)
        .WhereElementIsNotElementType()
        .WhereElementIsViewIndependent()
        .ToElements()
    )

    list_cat = []
    for ele in collector:
        if ele.Category is not None:
            current_int_cat_id = ele.Category.Id.IntegerValue
            if current_int_cat_id not in cat_exc:
                if not any(x._intId == current_int_cat_id for x in list_cat):
                    list_parameters = []
                    # Instance parameters
                    for par in ele.Parameters:
                        if (
                            par.Definition.BuiltInParameter
                            != DB.BuiltInParameter.ELEM_CATEGORY_PARAM
                            and par.Definition.BuiltInParameter
                            != DB.BuiltInParameter.ELEM_CATEGORY_PARAM_MT
                        ):
                            list_parameters.append(ParaInfo(0, par))
                    typ = ele.Document.GetElement(ele.GetTypeId())
                    # Type parameters
                    if typ is not None:
                        for par in typ.Parameters:
                            if (
                                par.Definition.BuiltInParameter
                                is not DB.BuiltInParameter.ELEM_CATEGORY_PARAM
                                and par.Definition.BuiltInParameter
                                is not DB.BuiltInParameter.ELEM_CATEGORY_PARAM_MT
                            ):
                                list_parameters.append(ParaInfo(1, par))
                    # Sort and add
                    list_parameters.sort(key=lambda x: x._name.upper(), reverse=False)
                    list_cat.append(CategInfo(ele.Category, list_parameters))
    list_cat.sort(key=lambda x: x._name, reverse=False)
    return list_cat


